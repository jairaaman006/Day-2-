// 1. Write a blog on the difference between document vs window object?

// document object

// *The document object represents a web page that is loaded in the browser.
// *By accessing the document object,we can access the element in html page.We can add dynamic content to our page.
// *It is accessed with window.document or document. Syntax- document.property_name

// Window object

// *Window object is the topmost object in DOM hierarchy.
// *It represents a browser window or a framework that displays the contents of the webpage.
// *Whenever a window appears on screen to display the contents of the document,window object is created. Syntax-window.document_name.
